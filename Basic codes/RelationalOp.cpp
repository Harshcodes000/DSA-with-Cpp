#include <iostream>
using namespace std;

int main() {
    int a, b;

    cout << "Enter two integers (a and b): ";
    cin >> a >> b;

    cout << "\n--- Relational Operators ---\n";
    cout << "a == b : " << (a == b) << "\n";
    cout << "a != b : " << (a != b) << "\n";
    cout << "a <  b : " << (a < b)  << "\n";
    cout << "a <= b : " << (a <= b) << "\n";
    cout << "a >  b : " << (a > b)  << "\n";
    cout << "a >= b : " << (a >= b) << "\n";

    return 0;
}



